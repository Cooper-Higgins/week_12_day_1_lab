public class Calculator {

    public int addIntegers(int num1, int num2){
        int Total = num1 + num2;
        return Total;
    }

    public int subtractIntegers(int num1, int num2){
        int Total = num1 - num2;
        return Total;
    }

    public int multiplyIntegers(int num1, int num2){
        int Total = num1 * num2;
        return Total;
    }

    public double divideIntegers(double num3, double num4){
        double Total = num3 / num4;
        return Total;
    }

}
